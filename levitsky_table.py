def get_maximum_weight(sex, height, age):
    '''
    :param sex: стать
    :param height: зріст
    :param age: вік
    :return: максимальна вага
    '''
    if sex == "man":
        return weights_for_man[str(height)][define_age(age)]
    elif sex == "woman":
        return weights_for_woman[str(height)][define_age(age)]

def define_age(age):
    '''
    :param age: вік
    :return: номер параметра
    '''
    number_of_age = -1
    if age >= 20 and age <= 29:
        number_of_age = 0
    elif age <= 39:
        number_of_age = 1
    elif age <= 49:
        number_of_age = 2
    elif age <= 59:
        number_of_age = 3
    elif age <= 69:
        number_of_age = 4
    return number_of_age